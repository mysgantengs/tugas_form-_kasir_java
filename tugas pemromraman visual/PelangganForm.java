    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String nama = txtNama.getText();
        String kelamin = rbLaki.isSelected() ? "Laki-laki" : "Perempuan";
        String telp = txtTelp.getText();
        String alamat = txtAlamat.getText();

    try {
        Connection con = koneksi.Koneksi.getKoneksi();
        String sql = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, jenis_kelamin, no_wa, alamat) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, id);
        pst.setString(2, nama);
        pst.setString(3, kelamin);
        pst.setString(4, telp);
        pst.setString(5, alamat);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
        loadTabel(); // fungsi untuk refresh table
        resetForm();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

