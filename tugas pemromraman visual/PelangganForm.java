    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
model.addColumn("ID");
model.addColumn("Nama");
model.addColumn("Jenis Kelamin");
model.addColumn("Telepon");
model.addColumn("Alamat");

try {
    String keyword = txtCari.getText(); // pastikan txtCari adalah nama field pencarian
    String sql = "SELECT * FROM pelanggan WHERE nama LIKE ?";
    Connection c = koneksi.Koneksi.getKoneksi();
    PreparedStatement p = c.prepareStatement(sql);
    p.setString(1, "%" + keyword + "%");
    ResultSet r = p.executeQuery();

    while (r.next()) {
        model.addRow(new Object[]{
            r.getString("id"),
            r.getString("nama"),
            r.getString("jk"),
            r.getString("telp"),
            r.getString("alamat")
        });
    }
    tablePelanggan.setModel(model);
} catch (Exception e) {
    System.out.println("Cari error: " + e);
}

    }//GEN-LAST:event_btnCariActionPerformed

